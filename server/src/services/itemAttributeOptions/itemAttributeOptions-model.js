const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const itemAttributeOptionsSchema = new Schema({
  name: { type: String, required: true },
  createdAt: { type: Date, 'default': Date.now },
  updatedAt: { type: Date, 'default': Date.now }
});

const itemAttributeOptionsModel = mongoose.model('itemAttributeOptions', itemAttributeOptionsSchema);

module.exports = itemAttributeOptionsModel;
